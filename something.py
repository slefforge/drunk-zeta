# The rewrite branch is currently in beta, and is not guaranteed to be stable.
# The rewrite branch can be installed with pip:
# `pip install -U git+
#
# This example requires the 'members' privileged intent to use the Member converter.
# Go to https://discord.com/developers/applications to create a new application.
# Click on the "Bot" tab, add a bot and copy the token.
# Repl.it is sensitive to spaces. Make sure there are no trailing spaces in the token.
# Click on the "OAuth2" tab.
# Under "scopes" tick the "bot" checkbox.
# Under "Bot Permissions" tick the checkboxes for "Send Messages" and "Embed Links".
